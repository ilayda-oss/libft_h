#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long    a;

	a = n;
	if (a < 0)
	{
		a *= -1;
		write(fd, "-", 1);
	}
	if (a >= 10)
	{
		ft_putnbr_fd(a / 10, fd);
	}
	write(fd, &"0123456789"[a % 10], 1);
}
